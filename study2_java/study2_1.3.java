class Main {
  public static void main(String[] args) {
    System.out.println(4 + 2 > 6);
    System.out.println(4 + 2 <= 6);
    
    System.out.println(true || false);
    System.out.println(false && true);
    System.out.println(8 < 5 && 3 >= 2);
    System.out.println(8 < 5 || 3 >= 2);
    System.out.println(!(8 < 5));
  }
}